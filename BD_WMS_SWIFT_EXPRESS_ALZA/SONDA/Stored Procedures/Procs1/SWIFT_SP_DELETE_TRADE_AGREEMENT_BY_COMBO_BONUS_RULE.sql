CREATE PROCEDURE [SONDA].[SWIFT_SP_DELETE_TRADE_AGREEMENT_BY_COMBO_BONUS_RULE](
	@TRADE_AGREEMENT_ID INT
	,@COMBO_ID INT
)
AS
BEGIN
	SET NOCOUNT ON;
	--
	DECLARE @TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID INT
	BEGIN TRY
		SELECT TOP 1 @TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID = [TAB].[TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID]
		FROM [SONDA].[SWIFT_TRADE_AGREEMENT_BY_BONUS_RULE] [TAB]
		INNER JOIN [SONDA].[SWIFT_TRADE_AGREEMENT_BY_COMBO_BONUS_RULE] [TAR] ON (
			[TAR].[TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID] = [TAB].[TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID]
		)
		WHERE [TAB].[TRADE_AGREEMENT_ID] = @TRADE_AGREEMENT_ID
			AND [TAR].[COMBO_ID] = @COMBO_ID
		--
		DELETE FROM [SONDA].[SWIFT_TRADE_AGREEMENT_BY_BONUS_RULE]
		WHERE [TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID] = @TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID
		--
		DELETE FROM [SONDA].[SWIFT_TRADE_AGREEMENT_BY_COMBO_BONUS_RULE]
		WHERE [TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID] = @TRADE_AGREEMENT_BONUS_RULE_BY_COMBO_ID
		--
		SELECT  1 as Resultado , 'Proceso Exitoso' Mensaje ,  0 Codigo, '' DbData
	END TRY
	BEGIN CATCH
		SELECT  -1 as Resultado
		,CASE CAST(@@ERROR AS VARCHAR)
			WHEN  '547' THEN 'El combo tiene bonificaciones asociadas'
			ELSE ERROR_MESSAGE()
		END Mensaje 
		,@@ERROR Codigo 
	END CATCH
END
