CREATE PROC [SONDA].[SWIFT_SP_INSERT_PORTFOLIO]
	@CODE_PORTFOLIO VARCHAR(25)
	, @NAME_PORTFOLIO VARCHAR(50)
	, @COMMENT VARCHAR(250)
	, @LAST_UPDATE_BY VARCHAR(50)
	, @pResult VARCHAR(250) OUTPUT
AS
BEGIN TRY
BEGIN
	IF EXISTS (SELECT 1 FROM [SONDA].SWIFT_PORTFOLIO WHERE CODE_PORTFOLIO = @CODE_PORTFOLIO) BEGIN
		SELECT @pResult = 'El codigo ya fue ingresado.'
		RETURN -1
	END
	BEGIN TRAN t1
		BEGIN		
			INSERT INTO [SONDA].SWIFT_PORTFOLIO(
				CODE_PORTFOLIO
				, NAME_PORTFOLIO
				, COMMENT
				, LAST_UPDATE
				, LAST_UPDATE_BY
			)
			VALUES(
				@CODE_PORTFOLIO
				, @NAME_PORTFOLIO
				, @COMMENT
				, GETDATE()
				, @LAST_UPDATE_BY
			)
			
		END	
	
	IF @@error = 0 BEGIN
		SELECT @pResult = 'OK'
		COMMIT TRAN t1
	END		
	ELSE BEGIN
		ROLLBACK TRAN t1
		SELECT	@pResult	= ERROR_MESSAGE()
	END
END
END TRY
BEGIN CATCH
     ROLLBACK TRAN t1
	 SELECT  -1 as Resultado , ERROR_MESSAGE() Mensaje ,  @@ERROR Codigo 
END CATCH
