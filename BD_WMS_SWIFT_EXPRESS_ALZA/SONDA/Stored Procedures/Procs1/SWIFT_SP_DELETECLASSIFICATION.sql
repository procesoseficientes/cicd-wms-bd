CREATE PROCEDURE [SONDA].[SWIFT_SP_DELETECLASSIFICATION]
@CLASSIFICATION INT,
@pResult varchar(250) OUTPUT
AS

DECLARE @NAME_CLASSIFICATION VARCHAR(50)
DECLARE @VALUE_TEXT VARCHAR(50)
DECLARE @MPC01 VARCHAR(50)
SET @NAME_CLASSIFICATION = (SELECT NAME_CLASSIFICATION FROM [SONDA].SWIFT_CLASSIFICATION WHERE CLASSIFICATION = @CLASSIFICATION)
SET @VALUE_TEXT = (SELECT VALUE_TEXT_CLASSIFICATION FROM [SONDA].SWIFT_CLASSIFICATION WHERE CLASSIFICATION = @CLASSIFICATION)
SET @MPC01 = (SELECT MPC01 FROM [SONDA].SWIFT_CLASSIFICATION WHERE CLASSIFICATION = @CLASSIFICATION)
IF ((SELECT COUNT(A.WEATHER_WAREHOUSE) FROM [SONDA].SWIFT_WAREHOUSES AS A WHERE A.WEATHER_WAREHOUSE =  @CLASSIFICATION )
+ (SELECT COUNT(B.CLASSIFICATION_LOCATION) FROM [SONDA].SWIFT_LOCATIONS AS B WHERE B.CLASSIFICATION_LOCATION = @CLASSIFICATION  )
+ (SELECT COUNT(C.CLASSIFICATION_SKU) FROM [SONDA].SWIFT_SKU AS C WHERE C.CLASSIFICATION_SKU = @CLASSIFICATION  )
+ (SELECT COUNT(D.CLASSIFICATION_CUSTOMER) FROM [SONDA].SWIFT_CUSTOMERS AS D WHERE D.CLASSIFICATION_CUSTOMER = @CLASSIFICATION  )
+ (SELECT COUNT(E.CLASSIFICATION_PROVIDER) FROM [SONDA].SWIFT_PROVIDERS AS E WHERE E.CLASSIFICATION_PROVIDER = @CLASSIFICATION  )
+ (SELECT COUNT(F.CLASSIFICATION_VEHICLE) FROM [SONDA].SWIFT_VEHICLES AS F WHERE F.CLASSIFICATION_VEHICLE = @CLASSIFICATION  )
+ (SELECT COUNT(G.TYPE_RECEPTION) FROM [SONDA].SWIFT_RECEPTION_HEADER AS G WHERE G.TYPE_RECEPTION = @MPC01  )
+ (SELECT COUNT(H.CLASSIFICATION_PICKING) FROM [SONDA].SWIFT_PICKING_HEADER AS H WHERE H.CLASSIFICATION_PICKING = @CLASSIFICATION  )
+ (SELECT COUNT(I.[USER_TYPE]) FROM [SONDA].USERS AS I WHERE I.[USER_TYPE] = @NAME_CLASSIFICATION )
) = 0
BEGIN 
	DELETE FROM SWIFT_CLASSIFICATION WHERE CLASSIFICATION=@CLASSIFICATION
	SELECT @pResult = ''
END
ELSE
	BEGIN
		SELECT @pResult = 'El dato no se puede eliminar debido a que está siendo utilizado'
	END
